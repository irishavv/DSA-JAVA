package Patterns;

public class circle {
    public static void main(String[] args) {
        for (int i = 3; i > 0; i--) {
            System.out.print(" ");
        }


        for (int j = 0; j < 3 - 2; j++) {
            System.out.print("* ");
        }
        System.out.println();
        System.out.print(" *   * ");
        System.out.println();
        System.out.print("*  *  * ");
        System.out.println();
        System.out.print(" *   * ");
        System.out.println();
        System.out.print("   *   ");
    }
}
